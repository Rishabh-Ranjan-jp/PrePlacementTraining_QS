class switchBreaksStatement{
	public static void main(String[] args){
		int x = 10;
		switch(x){
			case 0:
				System.out.println("0");
				break;
			case 10:
				System.out.println("1");
				break;
			case 20:
				System.out.println("2");
				break;
			default:
				System.out.println("fin");
		}
	}
}
